package com.CometEngine.Util.Meth;

public class CEFloat4f {
	public float x = 0;
	public float y = 0;
	public float z = 0;
	public float w = 1;

	public CEFloat4f() {
	
	}

	public CEFloat4f(float x, float y, float z, float w) {
		this.x = x;
		this.y = y;
		this.z = z;
		this.w = w;
	}

}
